			<h3>Screening Potential Residents</h3>

			<p class="text-justify" class="text-muted">Once an application for rental is received, it must be determined if the applicant is qualified to rent the property.  Has the applicant been evicted from a prior rental?  Does the applicant earn sufficient income to pay the rent?  These questions, as well as others should be answered prior to approving the applicant.</br></br>
			
			A professional property manager will typically check four areas when screening an applicant.  These are the applicant’s credit report, income, employment, and rental history.  It has become almost universal that the applicant’s credit report be checked for late pays, judgments, liens, or bankruptcies.  The applicant’s employer is typically contacted to verify income, and the applicant’s prior landlord is contacted to verify rental history.  While there are few perfect applicants, the information gleaned through the application review process can greatly reduce the likelihood of renting to an unqualified resident.</p>
			
			<h3>Preparation and Execution of Rental Documents</h3>

			<p class="text-justify" class="text-muted">After the applicant is qualified, the necessary rental documents must be prepared and executed.  Gone are the days of going to the local stationery store and buying a preprinted, two-page rental agreement.  Today, rental agreements are custom drafted to reflect specific state statutes, and sometimes, applicable local ordinances.  In addition to the rental agreement, any required addendums are prepared and may include an addendum relating to pets, a no smoking addendum, an addendum relating to pool use, and if the property was built prior to 1978, a lead paint addendum.  Further, a move-in inspection is completed and documented for review by the resident at execution of the rental agreement.  Ancillary forms relating to transfer of utilities and trash pick-up may also be needed depending on the location of the rental property.</p>
			
			<h3>Collection of Rent</h3>

			<p class="text-justify" class="text-muted">Every property owner would agree that timely payment of rent is critical.  If the applicant was properly screened prior to acceptance, most poor rental risks will be eliminated.  However, circumstance beyond the possibility of screening can adversely affect the resident’s ability to timely pay rent.  These circumstances include loss of employment, injury, illness, and changes in marital status.  When the resident is unable or unwilling to pay rent, the property manager has the ability to effective move forward with the collection process, or if that fails, terminate the rental agreement and locate a new resident in the minimum amount of time.</p>
			
			<h3>Disbursal of Rent to the Property Owner</h3>

			<p class="text-justify" class="text-muted">Typically, the property manager will receive rent on the first day of the month, with disbursal of funds to the property owner between the fifteenth and twenty-fifth of the month.  Because the property manager is required to hold all funds in trust, funds must be held until checks clear, which may take as many as ten working days.  In addition, time is required for the accounting and statement preparation process.</p>
			
